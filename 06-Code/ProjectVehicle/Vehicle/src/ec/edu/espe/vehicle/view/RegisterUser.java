    private static DBObject createDBObject(Register register) {
        BasicDBObjectBuilder docBuilder = BasicDBObjectBuilder.start();
        docBuilder.append("_DocumentBuyer", register.getDocumentBuyer());
        docBuilder.append("name", register.getName());
        docBuilder.append("lastName", register.getLastname());
        docBuilder.append("Amount of Sale", register.getAmountOfSale());
        docBuilder.append("Vehicle Sold", register.getVehicleSold());
        return docBuilder.get();
    }

