    public static boolean valida(String x) {
        int suma = 0;
        if (x.length() == 9) {
            System.out.println("Ingrese su cedula de 10 digitos");
            return false;
        } else {
            int a[] = new int[x.length() / 2];
            int b[] = new int[(x.length() / 2)];
            int c = 0;
            int d = 1;
            for (int i = 0; i < x.length() / 2; i++) {
                a[i] = Integer.parseInt(String.valueOf(x.charAt(c)));
                c = c + 2;
                if (i < (x.length() / 2) - 1) {
                    b[i] = Integer.parseInt(String.valueOf(x.charAt(d)));
                    d = d + 2;
                }
            }

            for (int i = 0; i < a.length; i++) {
                a[i] = a[i] * 2;
                if (a[i] > 9) {
                    a[i] = a[i] - 9;
                }
                suma = suma + a[i] + b[i];
            }
            int aux = suma / 10;
            int dec = (aux + 1) * 10;
            if ((dec - suma) == Integer.parseInt(String.valueOf(x.charAt(x.length() - 1)))) {
                return true;
            } else if (suma % 10 == 0 && x.charAt(x.length() - 1) == '0') {
                return true;
            } else {
                return false;
            }

        }
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtName.getText().equals("") || txtLastName.getText().equals("") || txtDocument.getText().equals("") || txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Empty fields");
        } else {
            if (valida(txtDocument.getText())) {
                try {
                    // TODO add your handling code here:

                    MongoClient mongo = new MongoClient("localhost", 27017);
                    DB db = mongo.getDB("journaldev");
                    DBCollection col = db.getCollection("users");
                    DBObject query = BasicDBObjectBuilder.start().add("document", txtDocument.getText()).get();
                    DBCursor cursor = col.find(query);
                    Register register = createUser((int) col.count() + 1, txtName.getText(), txtLastName.getText(), txtDocument.getText(), txtPassword.getText());
                    DBObject doc = createDBObject(register);
                    if (cursor.hasNext()) {
                        JOptionPane.showMessageDialog(null, "Existing User");
                    } else {
                        WriteResult result = col.insert(doc);
                        JOptionPane.showMessageDialog(null, "Registered user successfully");
                        txtDocument.setText("");
                        txtLastName.setText("");
                        txtName.setText("");
                        txtPassword.setText("");
                        Login login = new Login();
                        login.setVisible(true);
                        this.dispose();
                    }

                } catch (UnknownHostException ex) {
                    Logger.getLogger(RegisterUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Document Incorrect" );
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

